// Helper: get a city by slug. Returns null if not found OR if body is empty
// (the page won't render without unique content — anti-thin-content safeguard).
export const getCityBySlug = (slug) => {
  const city = CITIES.find((c) => c.slug === slug);
  if (!city) return null;
  if (!city.body || city.body.trim().length < 200) return null;
  return city;
};

// Helper: only return cities with content filled in. Used by sitemap and
// internal links so empty cities are never linked or indexed.
export const getPublishedCities = () =>
  CITIES.filter((c) => c.body && c.body.trim().length >= 200);
